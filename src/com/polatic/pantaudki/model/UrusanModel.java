package com.polatic.pantaudki.model;

public class UrusanModel {
    private String name;
    private String code;

    public UrusanModel(String name, String code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }
}
