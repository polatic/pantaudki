
package com.polatic.pantaudki.model;

public class ProgramModel {
    private String name;
    private String code;

    public ProgramModel(String name, String code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }
}
